  //     preload: IO_WINDOW_PRELOAD_WEBPACK_ENTRY,
  //     nodeIntegrationInWorker: true,
  //     nodeIntegration: true,
  //     contextIsolation: false,
  //   },
  //   autoHideMenuBar: true,
  // });

  // // eslint-disable-next-line @typescript-eslint/no-var-requires
